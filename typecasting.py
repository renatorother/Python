#tipos imutáveis: str, int, float e bool
print(1 + 1)
print('a'+'b')


print(int('1') + 1) #aqui converteu um str em int
print(type(int('1')), + 1) #aqui pedi para me mostrar o tipo do argumento, que no caso é uma str convertida em int + uma concatenação
print(type(float('1.1')), + 1) #aqui pedi para me mostrar o tipo do argumento, que no caso é uma str convertida em float + uma concatenação
print(str(11) + 'b') #aqui converteu um int em str
print(str(11.1) + 'b') #aqui converteu um float em str

